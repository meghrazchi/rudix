export function createAdminLandingTranslations(locale: SupportedLocale) {
  const ui = UI_COPY[locale];
  const titleMap = new Map<string, string>();
  if (locale !== "en")
    TITLES.forEach((title, index) =>
      titleMap.set(title, LOCALIZED_TITLES[locale][index] ?? title),
    );
  return { ...ui, text: (value: string) => titleMap.get(value) ?? value };
}
